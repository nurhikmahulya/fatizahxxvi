// NAVIGASI
function masuk() {
  document.getElementById("welcome").style.display = "none";
  document.getElementById("main").style.display = "block";
}

function kembali() {
  document.getElementById("main").style.display = "none";
  document.getElementById("welcome").style.display = "flex";
}

// FUNGSI
function getHari(tgl) {
  const hari = ["Minggu","Senin","Selasa","Rabu","Kamis","Jumat","Sabtu"];
  const [d,m,y] = tgl.split("-");
  return hari[new Date(`${y}-${m}-${d}`).getDay()];
}

function getTanggalLengkap(tgl) {
  const bulan = ["Januari","Februari","Maret","April","Mei","Juni","Juli","Agustus","September","Oktober","November","Desember"];
  const [d,m,y] = tgl.split("-");
  return `${d} ${bulan[m-1]} ${y}`;
}

function getUmur(tgl) {
  const [d,m,y] = tgl.split("-");
  const b = new Date(`${y}-${m}-${d}`);
  const t = new Date();
  let u = t.getFullYear() - b.getFullYear();
  if (t < new Date(t.getFullYear(), m-1, d)) u--;
  return u;
}

function getUltahBulanIni() {
  const bulan = new Date().getMonth() + 1;
  let hasil = anggota.filter(a => parseInt(a.tanggal_lahir.split("-")[1]) === bulan);

  hasil.sort((a,b) => parseInt(a.tanggal_lahir) - parseInt(b.tanggal_lahir));
  return hasil;
}

// RENDER
const container = document.getElementById("container");

anggota.forEach(data => {
  const card = document.createElement("div");
  card.className = "card";

  card.innerHTML = `
    <img src="${data.foto}">
    <h3>${data.nama_panggilan}</h3>
  `;

  card.onclick = () => {
    document.getElementById("modal").style.display = "block";
    document.getElementById("modalContent").innerHTML = `
      <img src="${data.foto}" class="profile-img">
      <h2>${data.nama_lengkap}</h2>
      <p>${getHari(data.tanggal_lahir)}, ${getTanggalLengkap(data.tanggal_lahir)}</p>
      <p>${getUmur(data.tanggal_lahir)} tahun</p>
      <p><b>Alumni ${data.kelas}</b></p>
      <p class="quote">@${data.quote}</p>
    `;
  };

  container.appendChild(card);
});

// ULTAH
